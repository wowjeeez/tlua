#[derive(PartialEq, Debug, Clone)]
pub struct Span {
    pub start: usize,
    pub end: usize,
}

#[derive(PartialEq, Debug, Clone)]
pub struct Token {
    pub kind: Tokens,
    pub span: Span,
}
#[derive(PartialEq, Debug, Clone)]
pub enum Tokens {
    And,
    Break,
    Do,
    Else,
    ElseIf,
    End,
    Type,
    Const,
    Class,
    Interface,
    Public,
    Private,
    Protected,
    Extends,
    Implements,
    Arrow,
    Switch,
    Qmark,
    For,
    Function,
    Goto,
    If,
    In,
    Local,
    Nil,
    Not,
    Or,
    Repeat,
    Return,
    Then,
    True,
    False,
    Until,
    While,
    IntDiv,
    Concat,
    Dots,
    Period,
    LParen,
    RParen,
    LCurly,
    RCurly,
    LBracket,
    RBracket,
    Comma,
    Plus,
    Minus,
    Mul,
    Div,
    Mod,
    Pow,
    BitAnd,
    BitOr,
    BitXor,
    Assign,
    NewLine,
    EQ,
    NEQ,
    GTE,
    LTE,
    LT,
    GT,
    SHL,
    SHR,
    Hash,
    SemiColon,
    DBColon,
    Colon,
    String(String),
    Number(f64),
    Ident(String),
    Comment(Comment),
    Unknown(String),
}

#[derive(PartialEq, Debug, Clone)]
pub enum Comment {
    SingleLine(String),
    MultiLine(String),
}